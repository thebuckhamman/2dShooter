package com.game;

/**
 * Used to define the powerup types
 * @author goofybud16
 *
 */
public enum PowerupType {
	/**
	 * Health powerups, provide +1 HP
	 */
	HEALTH,
	/**
	 * Levelup powerup, provide +1 Level
	 */
	ONE_LEVEL,
	/**
	 * Dual Levelup Powerup, provide +2 levels
	 */
	TWO_LEVELS,
	/**
	 * Time slowing powerup, slow time
	 */
	SLOW_TIME
}
